// Sync the shared provider + agent source into the extension as content scripts.
// The .user.js files are valid JS (the ==UserScript== header is just comments), so they
// load unchanged as MAIN-world content scripts. Run after editing either source file:
//   npm run build:extension
import { readFileSync, writeFileSync } from "node:fs";

const banner = "// AUTO-GENERATED by scripts/build-extension.mjs — edit the source in src/, not here.\n";

const pairs = [
  ["src/youtube-a11y-agent.user.js", "extension/provider.js"],
  ["src/agent/dev-agent.user.js", "extension/agent.js"],
];

for (const [from, to] of pairs) {
  writeFileSync(to, banner + readFileSync(from, "utf8"));
  console.log(`synced ${from} -> ${to}`);
}
console.log("Done. Reload the unpacked extension in chrome://extensions.");
